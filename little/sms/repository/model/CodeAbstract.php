declare(strict_types=1);

namespace little\sms\repository\model;

use littler\BaseModel as Model;
use littler\annotation\Inject;
use littler\traits\BaseOptionsTrait;
use littler\traits\RewriteTrait;

/**
 * @property id $int
 * @property phone $string 电话号码
 * @property code $int 验证码
 * @property is_used $int 0未使用  1已使用
 * @property create_time $int 创建时间
 * @property end_time $int 结束时间
 * @property update_time $int
 */
abstract class CodeAbstract extends Model
{
	use BaseOptionsTrait;
	use RewriteTrait;

	/**
	 * @var string $name 表名
	 */
	protected $name = 'sms_code';

	/**
	 * @var string $pk 主键
	 */
	public $pk = 'id';

	/**
	 * @var array $schema 字段信息
	 */
	protected $schema = [
		'id' => 'int',
		'phone' => 'string',
		'code' => 'int',
		'is_used' => 'int',
		'create_time' => 'int',
		'end_time' => 'int',
		'update_time' => 'int',
	];

	/**
	 * @var array $json JSON类型字段
	 */
	protected $json = [];

	/**
	 * @var array $field 允许写入字段
	 */
	public $field = ['id', 'phone', 'code', 'is_used', 'create_time', 'end_time', 'update_time'];
}
