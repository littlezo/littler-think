declare(strict_types=1);

namespace little\message\repository\model;

use littler\BaseModel as Model;
use littler\annotation\Inject;
use littler\traits\BaseOptionsTrait;
use littler\traits\RewriteTrait;

/**
 * @property member_id $int 用户ID
 * @property notice_id $int 文章ID
 */
abstract class NoticeReadAbstract extends Model
{
	use BaseOptionsTrait;
	use RewriteTrait;

	/**
	 * @var string $name 表名
	 */
	protected $name = 'message_notice_read';

	/**
	 * @var string $pk 主键
	 */
	public $pk = 'member_id';

	/**
	 * @var array $schema 字段信息
	 */
	protected $schema = ['member_id' => 'int', 'notice_id' => 'int'];

	/**
	 * @var array $json JSON类型字段
	 */
	protected $json = [];

	/**
	 * @var array $createTime 关闭创建时间自动写入
	 */
	protected $createTime = false;

	/**
	 * @var array $updateTime 关闭更新时间自动写入
	 */
	protected $updateTime = false;

	/**
	 * @var array $field 允许写入字段
	 */
	public $field = ['member_id', 'notice_id'];
}
