declare(strict_types=1);

namespace little\user\repository\model;

use littler\BaseModel as Model;
use littler\annotation\Inject;
use littler\traits\db\BaseOptionsTrait;
use littler\traits\db\RewriteTrait;

/**
 * @property id $int 主键
 * @property uid $int 操作用户ID
 * @property username $string 昵称
 * @property site_id $int 站点id
 * @property url $string 对应url
 * @property data $string 传输数据
 * @property ip $string ip地址
 * @property action_name $string 操作行为
 * @property create_time $int 创建时间
 */
abstract class UserLogsAbstract extends Model
{
	use BaseOptionsTrait;
	use RewriteTrait;

	/**
	 * @var string $name 表名
	 */
	protected $name = 'user_logs';

	/**
	 * @var string $pk 主键
	 */
	protected $pk = 'id';

	/**
	 * @var array $schema 字段信息
	 */
	protected $schema = [
		'id' => 'int',
		'uid' => 'int',
		'username' => 'string',
		'site_id' => 'int',
		'url' => 'string',
		'data' => 'string',
		'ip' => 'string',
		'action_name' => 'string',
		'create_time' => 'int',
	];

	/**
	 * @var array $json JSON类型字段
	 */
	protected $json = [];

	/**
	 * @var array $updateTime 关闭更新时间自动写入
	 */
	protected $updateTime = false;

	/**
	 * @var array $field 允许写入字段
	 */
	public $field = ['id', 'uid', 'username', 'site_id', 'url', 'data', 'ip', 'action_name', 'create_time'];
}
