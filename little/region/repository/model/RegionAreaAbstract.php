declare(strict_types=1);

namespace little\region\repository\model;

use littler\BaseModel as Model;
use littler\annotation\Inject;
use littler\traits\db\BaseOptionsTrait;
use littler\traits\db\RewriteTrait;

/**
 * @property id $int
 * @property pid $int 父级
 * @property name $string 名称
 * @property shortname $string 简称
 * @property longitude $string 经度
 * @property latitude $string 纬度
 * @property level $int 级别
 * @property sort $int 排序
 * @property status $int 状态1有效
 */
abstract class RegionAreaAbstract extends Model
{
	use BaseOptionsTrait;
	use RewriteTrait;

	/**
	 * @var string $name 表名
	 */
	protected $name = 'region_area';

	/**
	 * @var string $pk 主键
	 */
	protected $pk = 'id';

	/**
	 * @var array $schema 字段信息
	 */
	protected $schema = [
		'id' => 'int',
		'pid' => 'int',
		'name' => 'string',
		'shortname' => 'string',
		'longitude' => 'string',
		'latitude' => 'string',
		'level' => 'int',
		'sort' => 'int',
		'status' => 'int',
	];

	/**
	 * @var array $json JSON类型字段
	 */
	protected $json = [];

	/**
	 * @var array $createTime 关闭创建时间自动写入
	 */
	protected $createTime = false;

	/**
	 * @var array $updateTime 关闭更新时间自动写入
	 */
	protected $updateTime = false;

	/**
	 * @var array $field 允许写入字段
	 */
	public $field = ['id', 'pid', 'name', 'shortname', 'longitude', 'latitude', 'level', 'sort', 'status'];
}
