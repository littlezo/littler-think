namespace little\goods\api\controller;

use little\goods\repository\api\CartTrait;
use little\goods\service\api\CartService;
use littler\annotation\docs\ApiDocs;
use littler\annotation\Inject;
use littler\annotation\Route;
use littler\annotation\route\Group as RouteGroup;
use littler\annotation\route\Middleware;
use littler\BaseController as Controller;
use littler\Request;
use littler\Response;

/**
 * Class Cart.
 * @RouteGroup("api/goods")
 * @Middleware({littler\JWTAuth\Middleware\Jwt::class, "member"})
 * @apiDocs({
 *     "title": " 购物车",
 *     "version": "1.0.0",
 *     "layer": "api",
 *     "module": "goods",
 *     "group": "cart",
 *     "desc": "查询参数详见快速查询 字段含义参加字段映射"
 * })
 */
class Cart extends Controller
{
	use CartTrait;

	/**
	 * @Inject
	 * @var CartService
	 */
	protected $service;

	/**
	 * @Route("/cart/list", method="GET", ignore_verify=false)
	 * @apiDocs({
	 *     "title": "列表无分页",
	 *     "version": "v1.0.0",
	 *     "name": "list",
	 *     "headers": {
	 *         "Authorization": "Bearer Token"
	 *     },
	 *     "desc": "查询参数详见快速查询 字段含义参加字段映射",
	 *     "success": {
	 *         "code": 200,
	 *         "type": "success",
	 *         "message": "成功消息||success",
	 *         "timestamp": 1234567890,
	 *         "result": {
	 *             "encryptData": "加密数据自行解密",
	 *         },
	 *     },
	 *     "error": {
	 *         "code": 500,
	 *         "message": "错误消息",
	 *         "type": "error",
	 *         "result": "",
	 *         "timestamp": 1234567890
	 *     },
	 *     "param": {
	 *         "page": {
	 *             "required": false,
	 *             "desc": "页数",
	 *             "type": "int",
	 *             "default": 1,
	 *         },
	 *         "size": {
	 *             "required": false,
	 *             "desc": "单页数量",
	 *             "type": "int",
	 *             "default": 10,
	 *         }
	 *     }
	 * })
	 * @return \think\Response
	 */
	public function list(Request $request): ?\think\Response
	{
		return Response::success($this->service->list($request->get()));
	}

	/**
	 * @Route("/cart", method="POST", ignore_verify=false)
	 * @apiDocs({
	 *     "title": "加入购物车",
	 *     "version": "v1.0.0",
	 *     "name": "save",
	 *     "headers": {
	 *         "Authorization": "Bearer Token"
	 *     },
	 *     "desc": "查询参数详见快速查询 字段含义参加字段映射",
	 *     "success": {
	 *         "code": 200,
	 *         "type": "success",
	 *         "message": "成功消息||success",
	 *         "timestamp": 1234567890,
	 *         "result": {
	 *             "encryptData": "加密数据自行解密",
	 *         },
	 *     },
	 *     "error": {
	 *         "code": 500,
	 *         "message": "错误消息",
	 *         "type": "error",
	 *         "result": "",
	 *         "timestamp": 1234567890
	 *     },
	 *     "param": {
	 *         "sku_id": {
	 *             "required": false,
	 *             "desc": "商品规格ID",
	 *             "type": "int",
	 *             "default": "",
	 *         },
	 *         "num": {
	 *             "required": false,
	 *             "desc": "数量",
	 *             "type": "int",
	 *             "default": "",
	 *         }
	 *     }
	 * })
	 * @return \think\Response
	 */
	public function save(Request $request): ?\think\Response
	{
		return Response::success($this->service->save($request->post()));
	}

	/**
	 * @Route("/cart", method="DELETE", ignore_verify=false)
	 * @apiDocs({
	 *     "title": "购物车删除",
	 *     "version": "v1.0.0",
	 *     "name": "delete",
	 *     "headers": {
	 *         "Authorization": "Bearer Token"
	 *     },
	 *     "desc": "查询参数详见快速查询 字段含义参加字段映射",
	 *     "success": {
	 *         "code": 200,
	 *         "type": "success",
	 *         "message": "成功消息||success",
	 *         "timestamp": 1234567890,
	 *         "result": {
	 *             "encryptData": "加密数据自行解密",
	 *         },
	 *     },
	 *     "error": {
	 *         "code": 500,
	 *         "message": "错误消息",
	 *         "type": "error",
	 *         "result": "",
	 *         "timestamp": 1234567890
	 *     },
	 *     "param": {
	 *        "cart_ids": {
	 *             "required": true,
	 *             "desc": "购物车id",
	 *             "type": "array",
	 *             "default": 0,
	 *        }
	 *     }
	 * })
	 * @return \think\Response
	 */
	public function delete(Request $request): ?\think\Response
	{
		return Response::success($this->service->delete($request->param('cart_ids')));
	}
}
